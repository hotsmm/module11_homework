export let getCar = (n) => {
   let carName = n;
   if (carName == 1) {
      return carName = 'Honda';
   } else if (carName == 2) {
      return carName = 'Mazda';
   } else if (carName == 3) {
      return carName = 'Opel';
   } else if (carName == 4) {
      return carName = 'Toyota';
   } else if (carName == 5) {
      return carName = 'Reno';
   } else if (carName == 6) {
      return carName = 'Chevrolet';
   } else if (carName == 7) {
      return carName = 'Lexus';
   } else if (carName == 8) {
      return carName = 'Nissan';
   } else if (carName == 9) {
      return carName = 'Opel';
   } else if (carName == 10) {
      return carName = 'Peugeot';
   } else {
      return carName = 'Not a car';
   }
}